function insertImage() {
  let holdImages = "";
  arr.forEach(function (details) {
    holdImages += `<div id="box">
    <img src="${details.Image}" alt="${details.name}">
  </div>`;
  });
  document.querySelector("#container").innerHTML = holdImages;
}

function performSearchFunctionality() {
  let getSearchElement = document.querySelector("#searching");
  let bgcolor = document.querySelector("#bgc");
  getSearchElement.addEventListener("focus", function (details) {
    bgcolor.style.display = "block";
  });
  getSearchElement.addEventListener("blur", function (details) {
    bgcolor.style.display = "none";
  });
  getSearchElement.addEventListener("input", function (details) {
    const filteredArray = arr.filter((object) =>
      object.name.toLowerCase().startsWith(getSearchElement.value)
    );

    let filteredImages = "";
    filteredArray.forEach(function (data) {
      filteredImages += `<div id="serch-value">
      <i class="ri-search-line"></i>
      <p>${data.name}</p>
    </div>`;
    });
    document.querySelector("#show-search-result").style.display = "block";
    document.querySelector("#show-search-result").innerHTML = filteredImages;
    
    getSearchElement.addEventListener("blur", function (details) {
      document.querySelector("#show-search-result").style.display = "none";
    });
  });
}

insertImage();
performSearchFunctionality();